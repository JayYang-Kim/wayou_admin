package com.sp.admin;

public class AdminSessionInfo {
	private Long adminIdx;
	private String adminId;
	private String adminName;
	private int idnCode;
	private int departCode;
	public Long getAdminIdx() {
		return adminIdx;
	}
	public void setAdminIdx(Long long1) {
		this.adminIdx = long1;
	}
	public String getAdminId() {
		return adminId;
	}
	public void setAdminId(String adminId) {
		this.adminId = adminId;
	}
	public String getAdminName() {
		return adminName;
	}
	public void setAdminName(String adminName) {
		this.adminName = adminName;
	}
	public int getIdnCode() {
		return idnCode;
	}
	public void setIdnCode(int idnCode) {
		this.idnCode = idnCode;
	}
	public int getDepartCode() {
		return departCode;
	}
	public void setDepartCode(int departCode) {
		this.departCode = departCode;
	}
	
	
}
