package com.sp.store;

public class TicketDetail {
	private int ticketDetailCode;
	private String ticketDetailName;
	private int ticketCode;
	private int price;
	private int count;
	
	public int getTicketDetailCode() {
		return ticketDetailCode;
	}
	public void setTicketDetailCode(int ticketDetailCode) {
		this.ticketDetailCode = ticketDetailCode;
	}
	public String getTicketDetailName() {
		return ticketDetailName;
	}
	public void setTicketDetailName(String ticketDetailName) {
		this.ticketDetailName = ticketDetailName;
	}
	public int getTicketCode() {
		return ticketCode;
	}
	public void setTicketCode(int ticketCode) {
		this.ticketCode = ticketCode;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}

	
	
}
